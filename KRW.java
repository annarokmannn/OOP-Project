package currencyConverter;

public class KRW {
	
	public static double toUSD(double amount) {
		return amount * 0.00076;
	}
	public static double toRM(double amount) {
		return amount * 0.0036;
	}
	public static double toJPY(double amount) {
		return amount * 0.11;
	}
	public static double toEUR(double amount) {
		return amount * 0.00071;
	}
	public static double toIDR(double amount) {
		return amount * 11.90;
	}
	public static double toSGD(double amount) {
		return amount * 0.0010;
	}
	public static double toTL(double amount) {
		return amount * 0.022;
	}

}
